#include <iostream>
using namespace std;

int main() {
    int integer = 10;
    float floating_point = 10.5;
    double double_precision = 20.99;
    char character = 'A';

    cout << "Integer: " << integer << endl;
    cout << "Float: " << floating_point << endl;
    cout << "Double: " << double_precision << endl;
    cout << "Character: " << character << endl;

    return 0;
}